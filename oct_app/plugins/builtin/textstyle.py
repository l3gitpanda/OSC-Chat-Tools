def _apply_style(text, style):
    if style == "none" or style not in _STYLE_MAPS:
        return text
    char_map = _STYLE_MAPS[style]
    return "".join(char_map.get(c, c) for c in text)


def render_textstyle(context, text, data=0):
    style = context.get("text_style", "none")
    styled = _apply_style(text.replace("\\n", "\v").replace("\\v", "\v"), style)
    return context["check_data"](styled, data)


plugin = ChatboxPlugin(name="textstyle", render=render_textstyle)
